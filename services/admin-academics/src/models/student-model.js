import mongoose from 'mongoose';

const STUDENT_SCHEMA = new mongoose.Schema({
  niev: {
    type: String,
    required: true,
    trim: true,
  },
  name: {
    type: String,
    required: true,
    trim: true,
  },
  paternalSurname: {
    type: String,
    required: true,
    trim: true,
  },
  maternalSurname: {
    type: String,
    required: true,
    trim: true,
  },
  address: {
    type: String,
    required: true,
    trim: true,
  },
  curp: {
    type: String,
    required: true,
    trim: true,
  },
  email: {
    type: String,
    required: true,
    trim: true,
    unique: true,
  },
  phone: {
    type: String,
    required: true,
    trim: true,
  },
  username: {
    type: String,
    required: true,
    trim: true,
  },
});

export default mongoose.model('students', STUDENT_SCHEMA);
