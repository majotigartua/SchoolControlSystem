import mongoose from 'mongoose';

const USER_SCHEMA = new mongoose.Schema({
  username: {
    type: String,
    required: true,
    trim: true,
    unique: true,
  },
  email: {
    type: String,
    required: true,
    trim: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
    trim: true,
  },
});

export default mongoose.model('users', USER_SCHEMA);
